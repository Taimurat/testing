import java.util.regex.Matcher;
import java.util.regex.Pattern;

public class Main {
    public static void main(String[] args) {
        // 1. Строки, содержащие хотя бы одну цифру
        String regex1 = ".*\\d.*";

        // 2. Телефонные номера в формате (123) 456-7890 или 123-456-7890
        String regex2 = "\$?\\d{3}\$?[- ]?\\d{3}[- ]?\\d{4}";

        // 3. Самое длинное слово в строке
        String regex3 = "\\b\\w+\\b";

        // 4. Проверка, является ли строка целым числом
        String regex4 = "[+-]?\\d+";

        // 5. Строки, которые начинаются с числа и заканчиваются словом "end"
        String regex5 = "^[0-9].*end$";

        // 6. Проверка, является ли строка вещественным числом
        String regex6 = "[+-]?\\d*\\.\\d+";

        // 7. Строки, которые содержат более 5 символов и заканчиваются знаком "?"
        String regex7 = "^.{6,}\\?$";

        // 8. Строки, которые начинаются с любой буквы и заканчиваются знаком вопроса
        String regex8 = "^[a-zA-Z].*\\?$";

        // 9. Строки, которые начинаются со слова "Start" и заканчиваются точкой
        String regex9 = "^Start.*\\.$";

        // 10. Строки, которые содержат только буквы и пробелы
        String regex10 = "^[a-zA-Z ]+$";

        // 11. Строки, которые начинаются с заглавной буквы и заканчиваются восклицательным знаком
        String regex11 = "^[A-Z].*!$";

        // 12. Строки, которые начинаются с любой буквы и заканчиваются словом "done"
        String regex12 = "^[a-zA-Z].*done$";

        // 13. Строки, которые начинаются с фразы "Hello there" и заканчиваются знаком восклицания
        String regex13 = "^Hello there.*!$";

        // 14. Строки, состоящие исключительно из цифр и заканчивающиеся словом "done"
        String regex14 = "^\\d+ done$";

        // 15. Строки, которые начинаются с буквы, за которой следует одно или более чисел, и заканчиваются на слово "complete"
        String regex15 = "^[a-zA-Z]\\d+.*complete$";

        // 16. Строки, которые начинаются с символа # и заканчиваются словом "done"
        String regex16 = "^#.*done$";

        // 17. Строки, которые начинаются с пробела и заканчиваются на слово "exit"
        String regex17 = "^ .*exit$";

        // 18. Строки, которые начинаются с маленькой буквы и заканчиваются на слово "done"
        String regex18 = "^[a-z].*done$";

        // 19. Строки, содержащие хотя бы одну прописную букву английского алфавита
        String regex19 = ".*[A-Z].*";

        // Пример использования регулярных выражений
        String testString = "Что-то"; // Какой-то текст
        Pattern pattern = Pattern.compile(regex1); // regex1 заменяем на любое регулярное выражение
        Matcher matcher = pattern.matcher(testString);

        if (matcher.find()) {
            System.out.println("Совпадение найдено!");
        } else {
            System.out.println("Совпадение не найдено.");
        }
    }
}

